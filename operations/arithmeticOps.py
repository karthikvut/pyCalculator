"""Basic Arithmetic operations"""

num1 = int(input('Enter your first number: '))
num2 = int(input('Enter your second number: '))

# Addition
print('{0} + {1} = '.format(num1, num2))
print(num1 + num2)

# Subtraction
print('{0} - {1} = '.format(num1, num2))
print(num1 - num2)

# Multiplication
print('{0} * {1} = '.format(num1, num2))
print(num1 * num2)

# Division
print('{0} / {1} = '.format(num1, num2))
print(num1 / num2)